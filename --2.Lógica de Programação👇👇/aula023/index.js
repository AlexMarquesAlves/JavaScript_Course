/** if, else if & else
 */

const numero = 10;

if (numero >= 0 && numero <= 5) {
  console.log("El numero est치 entre 0 y 5.");
} else if (numero >= 6 && numero <= 8) {
  console.log("El numero est치 entre 6 y 8.");
} else if (numero >= 9 && numero <= 11) {
  console.log("El numero est치 entre 9 y 11.");
} else {
  console.log("El numero no est치 entre 0 y 11.");
}
